//! Bunting's thin, version-pinned adapter around `OrderBook-rs`.
//!
//! Matching, price levels, order types, trade generation, snapshots, replay helpers,
//! depth analytics, risk hooks, self-trade prevention, fees and the operational kill
//! switch are upstream responsibilities. Bunting owns authentication, canonical event
//! envelopes, participant accounting, persistence and Worker protocols.

